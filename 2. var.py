# for string we use "" while allocating a word to variable
# and for integer or decimals we simply write them without any punctuation marks
a="Hello sir"
b= "How are you"
c= "want to have a slice of pizza?"
# this is how to print multiple variables in one print statement
print(a,b,c)
# if you want to print with a space just write in another line
print(a,b)
print(c)
# you can also use sep to separate with any character
print(a,b,c, sep="-")


